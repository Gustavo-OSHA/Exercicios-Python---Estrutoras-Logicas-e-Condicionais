"""
Leia uma temperatura em graus Celsius e apresente-a convertida em graus Fahrenheit.
A formula de conversão é: F = C *(9/5) + 32 , sendo F a temperatura em Fahrenheit e 
C a temperatura em Celsius 
"""

C = float(input("Digite o valor Celsius:  "))
F = C *(9/5) + 32

print(f'Fahrenheit: {F}°F')


