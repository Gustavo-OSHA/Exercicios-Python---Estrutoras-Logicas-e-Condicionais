"""
Faça um programa que receba 2 numeros e mostre qual dele é o maior
"""

numero_1 = input("Digite um numero: ")
numero_2 = input("Digite um numero: ")

if numero_1 > numero_2:
    print(numero_1 + ' é maior que ' + numero_2 )
else:
    print(numero_1 + ' é menor que ' + numero_2 )    
